
export enum UserRole {
  DRIVER = 'DRIVER',
  PASSENGER = 'PASSENGER',
}

export type Gender = 'MALE' | 'FEMALE' | 'OTHER';

export interface Region {
  city: string;
  district: string;
  neighborhood: string;
}

export interface BaseUserInfo {
  id: string;
  role: UserRole;
  gender: Gender;
  job: string;
  contact: string;
  createdAt: number;
  introduction: string;
}

export interface DriverData extends BaseUserInfo {
  role: UserRole.DRIVER;
  activityRegions: Region[];
  carModel: string;
  schedule: string; // 요일 및 시간 정보 (e.g. 월~금 08:30)
}

export interface PassengerData extends BaseUserInfo {
  role: UserRole.PASSENGER;
  departure: Region;
  destination: Region;
  schedule: string; // 희망 요일 및 시간
  rewardType: string;
  keywords: string[];
}

export type CarpoolSubmission = DriverData | PassengerData;

